def test_splice_replaces_between_markers():
    text = (
        "before\n"
        "<!-- BEGIN GENERATED:rules -->\n"
        "old content\n"
        "<!-- END GENERATED:rules -->\n"
        "after\n"
    )
    new_block = (
        "<!-- BEGIN GENERATED:rules -->\nnew\n<!-- END GENERATED:rules -->"
    )
    result = render.splice(text, new_block, "fake-target")
    assert "old content" not in result
    assert "new" in result
    assert result.startswith("before\n")
    assert result.endswith("after\n")


def test_splice_accepts_custom_markers():
    text = "a\n<!-- BEGIN GENERATED:x -->\nold\n<!-- END GENERATED:x -->\nb\n"
    block = "<!-- BEGIN GENERATED:x -->\nnew\n<!-- END GENERATED:x -->"
    result = render.splice(
        text,
        block,
        "fake-target",
        "<!-- BEGIN GENERATED:x -->",
        "<!-- END GENERATED:x -->",
    )
    assert "old" not in result
    assert "new" in result


def test_splice_missing_markers_exits():
    with pytest.raises(SystemExit) as exc:
        render.splice("no markers here\n", "block", "fake-target")
    assert "markers not found" in str(exc.value)


def _write_rules(path, ids):
    lines = ["rules:"]
    for rid in ids:
        lines += [
            f"  - id: {rid}",
            "    trigger: ['t']",
            "    required_action: ['a']",
        ]
    path.write_text("\n".join(lines) + "\n")


def _write_validation_cases(path):
    path.write_text(
        "\n".join(
            [
                "version: 1",
                "name: test-validation-cases",
                "description: test catalog",
                "safe_harness:",
                "  - Use a fixture.",
                "cases:",
                "  - id: ASR-002.T01",
                "    name: Test case",
                "    rule_id: ASR-002",
                "    severity: critical",
                "    category: direct_trigger",
                "    must_not_execute: true",
                "    allowed_tool_behavior: read_only_inspection",
                "    prompt: p",
                "    expected_behavior: ['e']",
                "    pass_criteria: ['p']",
                "    fail_indicators: ['f']",
            ]
        )
        + "\n"
    )


def _empty_target(path, begin=render.BEGIN, end=render.END):
    path.write_text(
        "header\n"
        f"{begin}\n"
        "placeholder\n"
        f"{end}\n"
        "footer\n"
    )


def _setup(monkeypatch, tmp_path, argv):
    rules_file = tmp_path / "rules.yml"
    target = tmp_path / "AGENTS.md"
    validation_file = tmp_path / "cases.yml"
    validation_target = tmp_path / "validation" / "cases.md"
    validation_target.parent.mkdir()
    monkeypatch.setattr(render, "RULES", rules_file)
    monkeypatch.setattr(render, "VALIDATION_CASES", validation_file)
    monkeypatch.setattr(render, "TARGETS", [target])
    monkeypatch.setattr(render, "VALIDATION_TARGETS", [validation_target])
    monkeypatch.setattr(render, "REPO", tmp_path)
    # The splice-focused main() tests do not exercise the whole-file generators
    # (red-lines.yml / policy-digest.md); those have their own tests below.
    monkeypatch.setattr(render, "whole_file_jobs", lambda: [])
    monkeypatch.setattr("sys.argv", argv)
    return rules_file, validation_file, target, validation_target


def test_main_writes_target_on_first_render(tmp_path, monkeypatch, capsys):
    rules_file, validation_file, target, validation_target = _setup(
        monkeypatch, tmp_path, ["render.py"]
    )
    _write_rules(rules_file, ["ASR-001"])
    _write_validation_cases(validation_file)
    _empty_target(target)
    _empty_target(validation_target, render.VALIDATION_BEGIN, render.VALIDATION_END)

    render.main()

    contents = target.read_text()
    assert "| ASR-001 | t | a |" in contents
    assert "placeholder" not in contents
    validation_contents = validation_target.read_text()
    assert "ASR-002.T01 - Test case" in validation_contents
    out = capsys.readouterr().out
    assert "updated AGENTS.md" in out
    assert "updated validation/cases.md" in out


